    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String n = jTextField1.getText();
        String c = jTextField2.getText();
        String ex = jTextField3.getText();
        double costo = 0;
        double existencia = 0;

        try {
            costo = Double.parseDouble(c);
            existencia = Double.parseDouble(ex);
            ProductoPOJO pojo = new ProductoPOJO();
            pojo.setCosto(costo);
            pojo.setNombre(n);
            pojo.setExistencia(existencia);
            boolean x = ProductoJDBC.insertar(pojo);
            if (x == true) {
                JOptionPane.showMessageDialog(null, "Éxito");
                jDialog1.setVisible(false);
                cargarTabla();
                limpiaTF();
            } else {
                JOptionPane.showMessageDialog(null, "Error ");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en conversión");
        }

// TODO add your handling code here:
    }//GEN-LAST:event_jButton4ActionPerformed

